  "",
  "  // Guard + live-enforcement (працює навіть без Explorer)",
  "  (function(){",
  "    const HOME = computeHomePath();",
  "    const askOrRedirect = (folderName) => { const cover=document.createElement('div'); Object.assign(cover.style,{position:'fixed',inset:'0',zIndex:9997,background:'rgba(0,0,0,.5)',backdropFilter:'blur(8px)',WebkitBackdropFilter:'blur(8px)'}); document.body.append(cover); document.body.style.overflow='hidden'; showPasswordModal(folderName).then((ok)=>{ if(ok){ try{cover.remove();}catch(e){} document.body.style.overflow=''; grantAccess(folderName);} else { try{location.href=HOME;}catch(e){} } }); };",
  "    // одноразово при прямому вході",
  "    try { const mod0=currentModuleFromURL(); if(mod0 && isModuleFolder(mod0) && !hasAccess(mod0)) askOrRedirect(mod0); } catch(e){}",
  "    // live-перевірка (коли період змінився)",
