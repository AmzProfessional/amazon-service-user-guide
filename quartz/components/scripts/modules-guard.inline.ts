  "  const grantAccess = (folderName) => { localStorage.setItem(accessKey(folderName), JSON.stringify({ granted:true, expiresAt: periodEndISO() })); };",
  "",
  "  // Утиліти",
  "  const isModuleFolder = (name) => !!name && /^\\s*\\d+\\s*(модуль|module)/i.test(name.trim());",
  "  const currentModuleFromURL = () => { try { const path=decodeURIComponent(location.pathname||''); const first=path.replace(/^\\/+/, '').split('/')[0]||''; return first?first.replace(/-/g,' ').trim():''; } catch { return ''; } };",
  "  const computeHomePath = () => { const parts=(location.pathname||'/').split('/').filter(Boolean); if(parts.length===0 || /^\\d+/.test(parts[0])) return '/'; return '/'+parts[0]+'/'; };",
  "",
  "  // Модалка з блюром (білий текст для контрасту)",
  "  const showPasswordModal = (folderName) => new Promise((resolve) => {",
  "    let isDark=false; try { isDark=(window.matchMedia&&window.matchMedia('(prefers-color-scheme: dark)').matches)||document.documentElement.classList.contains('dark')||document.body.classList.contains('dark'); } catch(e){}",
  "    const overlay=document.createElement('div'); Object.assign(overlay.style,{position:'fixed',inset:'0',background:isDark?'rgba(0,0,0,.45)':'rgba(0,0,0,.35)',zIndex:9998,backdropFilter:'blur(6px)',WebkitBackdropFilter:'blur(6px)'});",
  "    const box=document.createElement('div'); Object.assign(box.style,{position:'fixed',top:'50%',left:'50%',transform:'translate(-50%,-50%)',background:isDark?'#1f1f1f':'#111',color:'#fff',padding:'20px',borderRadius:'14px',boxShadow:'0 12px 36px rgba(0,0,0,.35)',width:'min(520px,92vw)',zIndex:9999,fontFamily:'inherit'});",
  "    const inputStyle='width:100%;box-sizing:border-box;padding:12px 14px;border:1px solid '+(isDark?'#333':'#444')+';border-radius:10px;background:'+(isDark?'#2a2a2a':'#1b1b1b')+';color:#fff;outline:none';",
  "    const primaryBtn='flex:1;padding:12px 14px;border:0;border-radius:10px;cursor:pointer;background:#3a3aff;color:#fff';",
  "    const secondaryBtn='flex:1;padding:12px 14px;border:0;border-radius:10px;cursor:pointer;background:'+(isDark?'#2a2a2a':'#2b2b33')+';color:#ddd';",
  "    box.innerHTML = '<h3 style=\"margin:0 0 12px 0;color:#fff\">Введіть пароль для: <b>'+folderName+'</b></h3>' +",
  "      '<input id=\"module-pass\" type=\"password\" placeholder=\"Пароль\" style=\"'+inputStyle+'\">' +",
  "      '<div id=\"module-err\" style=\"color:#ff6b6b;margin:8px 0 0 0;display:none\">Невірний пароль</div>' +",
  "      '<div style=\"display:flex;gap:10px;margin-top:14px\">' +",
  "        '<button id=\"module-ok\" style=\"'+primaryBtn+'\">Увійти</button>' +",
  "        '<button id=\"module-cancel\" style=\"'+secondaryBtn+'\">Скасувати</button>' +",
  "      '</div>' +",
  "      '<div style=\"margin-top:10px;opacity:.85;font-size:.9em\">Доступ збережеться до кінця поточного періоду.</div>';",
  "    const cleanup=()=>{ document.body.style.overflow=''; overlay.remove(); box.remove(); };",
  "    document.body.append(overlay, box); document.body.style.overflow='hidden';",
  "    const input=box.querySelector('#module-pass'); const err=box.querySelector('#module-err');",
  "    const okBtn=box.querySelector('#module-ok'); const cancelBtn=box.querySelector('#module-cancel');",
  "    const submit=()=>{ const pass=(input&&input.value?input.value:'').trim(); if(pass===expectedPassword(folderName) || pass===getRotatingGlobalPassword()){ cleanup(); resolve(true);} else { if(err) err.style.display='block'; } };",
  "    okBtn && okBtn.addEventListener('click', submit);",
  "    input && input.addEventListener('keydown', (e)=>{ if(e && e.key==='Enter') submit(); });",
  "    cancelBtn && cancelBtn.addEventListener('click', ()=>{ cleanup(); resolve(false); });",
  "    overlay.addEventListener('click', ()=>{ cleanup(); resolve(false); });",
  "    input && input.focus();",
  "  });",
  "",
  "  // Хук на кліки (коли є Explorer)",
