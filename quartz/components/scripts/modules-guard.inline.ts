  "  })();",
  "",
  "  // Хоткеї у консоль: Ctrl+Alt+P (модуль), Ctrl+Alt+G (глобальний), Ctrl+Alt+A (усі паролі на головній)",
  "  document.addEventListener('keydown', (e)=>{",
  "    try{",
  "      if(!(e.ctrlKey && e.altKey && e.key)) return;",
  "      const key = e.key.toLowerCase();",
  "",
  "      // ===== Ctrl + Alt + P → пароль для модуля (із URL або вручну) =====",
  "      if(key==='p'){",
  "        const inf=currentModuleFromURL();",
  "        const folderName=(inf && /^\\s*\\d+\\s*(модуль|module)/i.test(inf)) ? inf : ((prompt('Вкажіть назву модуля (наприклад: 2 модуль)')||'').trim());",
  "        if(!folderName) return;",
  "        const pass=expectedPassword(folderName);",
  "        console.log('[ModulesGuard] Модуль:', folderName, '| Період:', periodIndex(), '| Пароль:', pass);",
  "        return;",
  "      }",
  "",
  "      // ===== Ctrl + Alt + G → глобальний пароль (12 символів) =====",
  "      if(key==='g'){",
  "        const g=getRotatingGlobalPassword();",
  "        console.log('[ModulesGuard] Глобальний пароль (12 символів):', g, '| Період:', periodIndex());",
  "        return;",
  "      }",
  "",
  "      // ===== Ctrl + Alt + A → ВИВЕСТИ ВСІ ПАРОЛІ (тільки на головній) =====",
  "      if(key==='a'){",
  "        const isHome = (()=>{",
  "          const HOME = computeHomePath();",
  "          let p = location.pathname || '/';",
  "          if(!p.endsWith('/')) p += '/';",
  "          return p === HOME;",
  "        })();",
  "        if(!isHome){",
  "          console.warn('[ModulesGuard] Ctrl+Alt+A працює лише на головній сторінці сайту. Перейдіть на', computeHomePath());",
  "          return;",
  "        }",
  "",
  "        // Зібрати назви модулів з посилань на сторінці",
  "        const findModulesOnPage = ()=>{",
  "          const set = new Set();",
  "          document.querySelectorAll('a[href]')?.forEach((a)=>{",
  "            try{",
  "              const href = (a as HTMLAnchorElement).getAttribute('href') || '';",
  "              if(!href) return;",
  "              // нормалізуємо шлях, беремо перший сегмент",
  "              let path = href;",
  "              // абсолютні/відносні/якоря — усе приймаємо",
  "              if(/^https?:/i.test(path)){",
  "                try{ path = new URL(path).pathname; }catch(_){ return; }",
  "              }",
  "              const first = decodeURIComponent(path).replace(/^\\/+/, '').split('/')[0] || '';",
  "              if(!first) return;",
  "              const name = first.replace(/-/g,' ').trim(); // \"1-модуль\" -> \"1 модуль\"",
  "              if(isModuleFolder(name)) set.add(name);",
  "            }catch(_){ /* ignore */ }",
  "          });",
  "          // відсортуємо за номером модуля (якщо є)",
  "          const arr = Array.from(set);",
  "          arr.sort((a,b)=>{",
  "            const na = parseInt((a.match(/\\d+/)||['0'])[0],10);",
  "            const nb = parseInt((b.match(/\\d+/)||['0'])[0],10);",
  "            return na-nb || a.localeCompare(b,'uk');",
  "          });",
  "          return arr;",
  "        };",
  "",
  "        let modules = findModulesOnPage();",
  "        if(!modules.length){",
